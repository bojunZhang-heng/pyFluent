import numpy as np

###############################################################################
# axis introduction
# ~~~~~~~~~~~~~~~~~
#

# 一维数组: 只有 axis=0
arr_1d = np.array([1, 2, 3, 4, 5])
# 轴: 0 → [1, 2, 3, 4, 5]

# 二维数组: axis=0(行), axis=1(列)
arr_2d = np.array([[1, 2, 3],
                   [4, 5, 6]])
# axis=0 ↓  axis=1 →
#        [1, 2, 3]
#        [4, 5, 6]

# 三维数组: axis=0, axis=1, axis=2
arr_3d = np.array([[[1, 2], [3, 4]],
                   [[5, 6], [7, 8]]])

